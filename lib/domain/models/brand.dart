class Brand{

  final int id;
  final String name;
  final String description;
  final String logo;
  final String type;
  final String facebookPageLink;
  final String instagramPageLink;
  final String? websitePageLink;
  final String mobileNumber;
  final String email;
  final String? taxIdNumber;

  Brand({required this.id, required this.name, required this.description, required this.logo, required this.type, required this.facebookPageLink, required this.instagramPageLink, required this.websitePageLink, required this.mobileNumber, required this.email, required this.taxIdNumber});

}